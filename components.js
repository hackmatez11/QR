// Get category label
function getCategoryLabel(category) {
    const categories = {
        photo: 'Photo',
        report: 'Lab Report',
        prescription: 'Prescription',
        xray: 'X-Ray',
        mri: 'MRI/CT Scan',
        other: 'Other'
    };
    return categories[category] || category;
}

// Download file function
window.downloadFile = function (url, filename) {
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    a.click();
};

